def add(a,b):
    total = a+b
    return total

def subtract(c,d):
    diff = c-d
    return diff

def multiply(e,f):
    prod = e*f
    return prod

def divide(g,h):
    q = g/h
    return q

if __name__ == "__main__":
    print(add(67,87))
    print(subtract(78,56))
    print(multiply(34,23))
    print(divide(24,6))
